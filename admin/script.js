const body = document.querySelector("body"),
      modeToggle = body.querySelector(".mode-toggle");
      sidebar = body.querySelector("nav");
      sidebarToggle = body.querySelector(".sidebar-toggle");

let getMode = localStorage.getItem("mode");
if(getMode && getMode ==="dark"){
    body.classList.toggle("dark");
}

let getStatus = localStorage.getItem("status");
if(getStatus && getStatus ==="close"){
    sidebar.classList.toggle("close");
}

modeToggle.addEventListener("click", () =>{
    body.classList.toggle("dark");
    if(body.classList.contains("dark")){
        localStorage.setItem("mode", "dark");
    }else{
        localStorage.setItem("mode", "light");
    }
});

sidebarToggle.addEventListener("click", () => {
    sidebar.classList.toggle("close");
    if(sidebar.classList.contains("close")){
        localStorage.setItem("status", "close");
    }else{
        localStorage.setItem("status", "open");
    }
});

 const productImageFile = document.getElementById('productImageFile');
    const imagePreview = document.getElementById('imagePreview');
    const uploadIconContainer = document.getElementById('uploadIconContainer');

    // Handle image preview
    productImageFile.addEventListener('change', function(event) {
        const file = event.target.files[0];
        if (file) {
            const reader = new FileReader();
            reader.onload = function(e) {
                imagePreview.src = e.target.result;
                imagePreview.classList.remove('hidden');
                uploadIconContainer.classList.add('hidden'); // Hide the icon and placeholder text
            }
            reader.readAsDataURL(file);
        } else {
            imagePreview.src = "#";
            imagePreview.classList.add('hidden');
            uploadIconContainer.classList.remove('hidden'); // Show icon if no file selected
        }
    });